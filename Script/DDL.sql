CREATE TABLE Movie (
    movie_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    start_year NUMBER,
    average_rating NUMBER,
    movie_name VARCHAR2(50),
    PRIMARY KEY (movie_id)
);

CREATE TABLE Release (
    release_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    movie_id NUMBER,
    region VARCHAR2(50),
    language VARCHAR2(50),
    CONSTRAINT FK_movie_id1 FOREIGN KEY (movie_id) REFERENCES Movie (movie_id),
    PRIMARY KEY (release_id)
);

CREATE TABLE Movie_has_Person (
    movie_id NUMBER,
    person_id NUMBER,
    CONSTRAINT FK_movie_id3 FOREIGN KEY (movie_id) REFERENCES Movie (movie_id),
    CONSTRAINT FK_person_id1 FOREIGN KEY (person_id) REFERENCES PERSON (person_id),
    PRIMARY KEY (movie_id, person_id)
);

CREATE TABLE Genres (
    genres_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR2(50),
    PRIMARY KEY (genres_id)
);

CREATE TABLE Movie_has_Genres (
    movie_id NUMBER,
    genres_id NUMBER,
    CONSTRAINT FK_movie_id4 FOREIGN KEY (movie_id) REFERENCES Movie (movie_id),
    CONSTRAINT FK_genres_id1 FOREIGN KEY (genres_id) REFERENCES Genres (genres_id),
    PRIMARY KEY (movie_id, genres_id)
);





